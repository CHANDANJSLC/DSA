package dsa;
import java.util.*;
public class Sum_of_n_natural_numbers {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		
		int n= scan.nextInt();
		int sum=(n*(n+1))/2;//Logic for sum of n natural numbers.
		
		System.out.println(sum);
		System.out.println(sum);
	}
}
